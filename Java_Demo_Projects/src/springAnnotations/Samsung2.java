package springAnnotations;

public class Samsung2 {
	
	private int RAM;
	private Camera2 CAM;
	private String MODEL;
	public int getRAM() {
		return RAM;
	}
	public void setRAM(int rAM) {
		RAM = rAM;
	}
	public Camera2 getCAM() {
		return CAM;
	}
	public void setCAM(Camera2 cAM) {
		CAM = cAM;
	}
	public String getMODEL() {
		return MODEL;
	}
	public void setMODEL(String mODEL) {
		MODEL = mODEL;
	}
	
}
